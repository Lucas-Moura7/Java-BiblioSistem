    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
         Usuario u = new Usuario();
       u.setNome(txtLogin.getText());
       u.setSenha(txtSenha.getText());
       
       u = UsuarioJPA.validarUsuario(u);
       
       if(u != null) {
           JOptionPane.showMessageDialog(null, "Usuario " + u.getNome() + " autenticado com sucesso!");
           TelaPrincipal tl = new TelaPrincipal();
        tl.setVisible(true);
       }else {
           JOptionPane.showMessageDialog(null, "Usuario n√£o encontrado!");
       }
        
    }//GEN-LAST:event_btnLoginActionPerformed

